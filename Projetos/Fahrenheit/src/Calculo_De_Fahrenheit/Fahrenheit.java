package Calculo_De_Fahrenheit;

public class Fahrenheit {
	public static void main (String[] args) {
		
		double Fahrenheit;
		double Celsius;
		
		Celsius = 27f;
		Fahrenheit = (9 * Celsius + 160) / 5f;
		
		System.out.print("A temperatura hoje está de " + Fahrenheit + " Graus Fahrenheits");
		
		
	}

}
