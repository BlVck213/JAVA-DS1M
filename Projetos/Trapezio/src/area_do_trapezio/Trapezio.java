package area_do_trapezio;

public class Trapezio {
	  public static void main (String[] args) {
		   
		double Base_Maior;
		double Base_Menor;
		double Altura;
		double Area;
		
		Base_Maior = 20;
		Base_Menor = 15;
		Altura = 10;
		Area = (Base_Maior + Base_Menor) * Altura / 2;
		
		System.out.print(Area);
	  }

}
